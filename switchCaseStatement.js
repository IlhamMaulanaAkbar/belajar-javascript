let area = 4;

switch (area) {
    case 1:
        console.log("Kawasan Bebas Rokok");
        break;
    case 2:
        console.log("Kawasan Dilarang Merokok");
        break;
    case 3:
        console.log("Kawasan Pengunjung");
        break;
    case 4:
        console.log("Kawasan Karyawan");
        break;
    default:
        console.log("Kawasan Bebas");
}

